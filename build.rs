fn generate_pretokenizer_config(
    writer: &mut BufWriter<File>,
    config: &TokenizerConfig,
) -> Result<()> {
    writeln!(writer, "// Pre-tokenizer configuration")?;
    
    if let Some(pre_tokenizer) = &config.pre_tokenizer {
        writeln!(writer, "pub const METASPACE_REPLACEMENT: &str = {:?};", pre_tokenizer.replacement)?;
        writeln!(writer, "pub const ADD_PREFIX_SPACE: bool = {};", pre_tokenizer.add_prefix_space)?;
    } else {
        writeln!(writer, "pub const METASPACE_REPLACEMENT: &str = {:?};", METASPACE_REPLACEMENT)?;
        writeln!(writer, "pub const ADD_PREFIX_SPACE: bool = true;")?;
    }
    writeln!(writer)?;
    
    // Generate special token set for fast lookup without cloning
    writeln!(writer, "// Special token set for fast lookup")?;
    writeln!(writer, "pub static SPECIAL_TOKEN_SET: Lazy<HashMap<u32, &'static str>> = Lazy::new(|| {{")?;
    writeln!(writer, "    let mut map = HashMap::new();")?;
    
    for token in &config.added_tokens {
        if token.special {
            writeln!(writer, "    map.insert({}, {:?});", token.id, &token.content)?;
        }
    }
    
    writeln!(writer, "    map")?;
    writeln!(writer, "}});")?;
    writeln!(writer)?;
    
    Ok(())
}